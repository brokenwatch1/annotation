package com.search.selfannotaion.bean;

public class Job {
	private String id;
	private String job;
	
	public Job(String id, String job) {
		this.id = id;
		this.job = job;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getJob() {
		return job;
	}
	public void setJob(String job) {
		this.job = job;
	}
	
	
	
	
}
